    private void remember() {
        preference = Preferences.userNodeForPackage(this.getClass());
        remember = preference.getBoolean("RememberMe", Boolean.valueOf(""));
        if (remember) {
            user.setText(preference.get("User", ""));
            pass.setText(preference.get("Pass", ""));
            this.RememberMe.setSelected(remember);
        }
    }

